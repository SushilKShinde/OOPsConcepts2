using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOPsConcepts2
{
    public class Person
    {
        private string name;
        private int age;

        to get and set the value of name

        public string getName
        {
            get { return name; }
            set { name = value; }
        }

        to get and set the value of age
        public int getAge
        {
            get { return age; }
            set { age = value; }
        }
    }
}
